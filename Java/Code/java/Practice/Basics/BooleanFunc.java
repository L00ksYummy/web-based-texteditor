public class BooleanFunc
{
    public static void main(String[] args) {
        int x = 1;
        int y = 4;

        if ((x == 1) && (y == 4)) // Must use == for if statement to work
        {
            System.out.println("True");
        }
        else
        {
            System.out.println("False");
        }
    }
}